                        Culture plays a pivotal role in the food scene in Sri Lanka while many Sinhalese food items are derived from the chena cultivation. From the most celebrated and auspicious Sinhala and Hindu New Year to various day to day events and celebrations will see an array of traditional Sri Lankan meals ranging from milk rice, sweetmeats and spicy curries for accompaniments. <br>
