         <section class="spices" id="spices">
            <div class="title reveal">
                <h2 class="section-title">Spices</h2>
            </div>
            <div class="content">
               

                </div>
                <div class="column col-right reveal">
                <h2 class="content-title">Hey there! I'm chalana nimsara</h2>
                    <p class="paragraph-text">Herbs and spices have been a major influence on Eastern cuisine since times immemorial. Spices heighten the flavour and aroma of a dish. Currently, herbs and spices are valued beyond their rudimentary flavouring applications and see continued demand for various medicinal purposes. <br>
                    As the name 'Spice Island' implies, Sri Lanka is no stranger to the world of herbs and spices. In fact, it is no exaggeration to state that spices have influenced the cuisine and the taste buds of the local communities with their rich flavour profiles and aromatic properties. <br>
                    The 'Spice Island' came to be as a result of Sri Lanka's climatic conditions allowing for a variety of spices to be grown on the Island's soils. The abundance of these culinary treasures attracted the attention of many western nations throughout history who wished to source from Sri Lanka’s spice market.<br>
                    Today, Sri Lanka's spice economy continues to flourish having penetrated the foreign markets which can't help but be allured by the rich aroma of Sri Lankan spices. Sri Lanka supplies the international market with some of the most sought-after spices and allied products such as cinnamon, pepper, cloves, cardamoms, nutmeg, mace and vanilla. Sri Lanka's spice exports have grown to amount to over 50% of all agricultural exports and continue to see upward trends as they find their way into the kitchens all over the globe. <br>
                    These are 12 major Herbs and spices found in Sri Lanka. <br><br>
                    <h2>CINNAMON</h2> <br>
                    If there is a spice that Sri Lanka is particularly famous for, it’d undoubtedly be Cinnamon. 'Ceylon Cinnamon’ is world-renowned for its notable qualities such as unique flavour, colour and aroma in comparison with Cassia. It has been a major spice in Sri Lanka culture for years as a food preservative and later for its natural flavouring properties. <br>
                    These thin brown pieces of bark emanate a sweet fragrance and are commonly found in bakery products, flavoured teas or meat preservation. It is not only an excellent elevator but also an acknowledged health blessing as it possesses medical properties to treat chronic diseases such as diabetes and cancer. It also contains some anti-inflammatory properties. <br>
                    As Sri Lanka's most important spice export Ceylon Cinnamon not only sees exports in its natural form but is also processed into tablets, powder or essential oils to be exported to major markets, the value and importance placed on Ceylon Cinnamon rank Sri Lanka as the source for 90% of the true cinnamon exported around the world. <br><br>
                    <h2>PEPPER</h2> <br>
                    The king of spice, Pepper has the second-largest spice presence in Sri Lanka and is the largest spice presence in the global market. Obtained from Piper Nigrum vines, this spice is easily recognizable for its sharp aroma and mildly spicy flavour that often let it substitute for chillies. In addition to flavouring, pepper is ripe with medicinal properties and is often used in massages or other natural treatment methods. <br><br>
                    <h2>CURRY LEAVES</h2> <br>
                    Curry leaves or 'Karapincha' as referred to by locals come from a commonplace plant that can be found in almost any Sri Lankan home garden. These freshly plucked leaves give out a strong fragrance when tempered making them an ideal seasoning. <br>
                    As the name suggests, Curry leaves are an excellent addition to curry-based dishes that dominate Sri Lankan and Eastern cuisines. Curry leaves are most effective when freshly plucked, which lead to them being grown in many home gardens bringing a variety of medicinal properties against liver diseases, cholesterol etc. while also lifting the dish's flavours to new heights. <br><br>
                    <h2>PANDAN LEAVES</h2> <br>
                    The Pandan leaves or 'Rampe' belong to a variety of screw-pine plants. The blade-like leaves of this plant possess an intense nutty aroma that makes it an ideal accompaniment for rice or curry-based dishes. Whatever the application, Pandan plants are a simple yet effective means to add aroma and flavour to a dish and are sure to fill your entire kitchen with its exotic fragrance. <br><br>
                    <h2>LEMONGRASS (SERA)</h2> <br>
                    A tropical herb with culinary properties, lemongrass consists of long thick stalks of grass that produce an essential oil with an odour similar to that of lemon which is where its name is derived from. Lemongrass harmonizes meat and savoury dishes making it one of the most widely used herbs in Sri Lankan cooking where its lower bulbous portions are used, pounded or cut into slices. <br>
                    In addition to cooking Lemongrass also acts as a diuretic, tonic or stimulant and exhibits various other medicinal properties, and is even utilized as an insect repellant making it one of the most versatile herbs grown in Sri Lanka. <br><br>
                    <h2>CLOVES (KARAMBUNATTI)</h2> <br>
                    The dried unopened flower buds of an evergreen tree, this spice is easily identified by its incredibly intense spicy aroma and pungent bittersweet flavours. Cloves are an essential spice in Eastern cuisine and are primarily used to flavour and aromatize meat or rice-based dishes. <br>
                    Cloves are also known to be applied as medicines for dental applications and relieving toothaches or aid with digestion difficulties or processed to form cosmetics such as perfume, soap and toothpaste. Sri Lanka commands significant attention within the cloves market as a result of displaying richer quality oils found in Sri Lankan cloves as opposed to other growers. <br> <br>
                    <h2>CARDAMOM</h2> <br>
                    A spice among locals for its ability to send your taste buds into a spicy shock when bitten Cardamom is a small seed pod with black seeds inside that extrude its famous sweet fragrance. It is widely used in various meat curries and yellow rice dishes and is further utilized in making desserts or even added to your evening tea for a bit of spice. <br>
                    As one of the most valued spices in the world alongside saffron and vanilla, Cardamom truly lives up to its title of 'The Queen of Spice' with its broad culinary and medicinal uses. <br> <br>
                    <h2>CHILLI</h2> <br>
                    Simple yet effective chilli is an easily accessible avenue to pack a spicy punch into a dish. These fiery capsicums come in a wide range of colours, sizes and most importantly hotness. Sri Lanka cultivates several varieties of chillies that are widely used in Sri Lankan cuisine ranging from the famous red chillies that can be used in almost any dish powdered, sliced or even dried for preservation and an extra crunch to green chillies which are known to be even hotter and contain some added zest. <br> <br>
                    <h2>CUMIN</h2> <br>
                    Cumin is a spice that is made from the harvested seed of Cuminum Cyminum plants, Cumin is one of the most prominent spices around the world as these tiny boat-shaped seeds have historically seen use in Middle Eastern and Asian cuisines. The most common variety of cumin traded around the world is of the yellow-brown variety which is used for the preparation of dishes while other varieties such as the sweeter black cumin often seen used in the making of dessert. <br><br>
                    As a flavouring medium cumin is often used in roasted or ground form, toasting the cumin seeds has been found to be the most successful method of releasing their warm earthy aroma and sweet-spicy flavour that lends itself as an excellent accompaniment for vegetable curries or meat-based dishes. Beyond just a taste, elevator cumin is also known to possess distinct medicinal properties to aid against digestive difficulties, blood cholesterol or diabetes. <br><br>
                    <h2>CORIANDER</h2> <br>
                    A delicate herb found in almost every vegetable garden in Sri Lanka Coriander has been a staple in Sri Lankan cuisine for centuries. Traditionally both the Coriander seeds and leaves have been utilized as a stimulant and spice or for its medicinal properties against upset stomachs, nausea, bacterial infections and more. <br>
                    As a spice coriander seeds are known for containing a rather spicy citrus flavour, when used in culinary prospects the seeds are first roasted and ground into a fine powder to use in broth-based dishes and is an essential ingredient in Sri Lanka Curry powder. <br>
                    Coriander is certainly a chef's delight as the entire plant from leaves to stems are edible and filled with a tangy citrus smell making it an excellent addition to curries or salads and it is a very common sight to see torn Coriander leaves being utilized as a garnish in almost any high-end restaurant around the world. <br><br>
                    <h2>NUTMEG AND MACE</h2> <br>
                    Originating from the same tree Nutmeg and Mace are siblings in the spice world, nutmeg is the oval-shaped fruit while the mace is the reddish webbing that wraps around the pit. Nutmeg and mace are perhaps most famous for their applications in desserts such as cakes and pies but also find themselves in quite a few savoury applications such as sausages or stews and are notably an essential ingredient for some seasonal Christmas beverages like eggnog. <br><br>
                    <h2>GINGER</h2> <br>
                    A spice that needs no introduction ginger is currently one of the most widely used spices in the world as more and more regional cuisines integrate ginger into their dishes. The spice is extracted from the thick gnarly roots of the ginger plant, originating from Asian regions it has been a favourite ingredient for both chefs and doctors thanks to its sheer versatility in both culinary and medicinal applications. <br>
                    A staple in Sri Lankan and Asian cuisine as whole ginger imparts a tart flavour with some peppery heat and mild sweetness that mellows when cooked. Ginger is used to tenderizing and flavour meats and adds complexity to any dish it's used in. Ginger can also be ground into a fine powder to be used in making desserts or sliced and steeped with hot water to make ginger tea which is a popular beverage rich with health benefits. <br>
                    Beyond its use as a spice, ginger is renowned for its array of health benefits it is a known Antioxidant and has been found to greatly alleviate digestion difficulties and nausea, reduce muscle pains and inflammation while ginger extract helps with supporting cardiovascular health. <br>

