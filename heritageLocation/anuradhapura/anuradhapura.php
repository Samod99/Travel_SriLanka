                    <h2 class="content-title">Transportation</h2>
                    * Anuradhapura is served by railway and highways. The Northern railway line connects Anuradhapura with Colombo, Jaffna, and Kankesanthurai. Anuradhapura railway station is the city's rail gateway, with major services, such as the Yal Devi, Uttara Devi stopping there. <br>
                    * There are number of bus routes passing through Anuradhapura from Colombo to northern province . Some of them are 04, 15, 57, 87 etc. <br>
                    * Anuradhapura is a central city in Sri Lanka. It is directly connected by road to a large number of major cities and towns on the island. By road, it is connected to Vavuniya, Dambulla, Matale, Puttalam, Trincomalee, Jaffna, Kurunegala and Kandy.
