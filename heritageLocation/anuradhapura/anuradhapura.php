                <h2 class="content-title">Anuradhapura</h2>
                <p class="paragraph-text">* Anuradhapura (Sinhala: අනුරාධපුරය, romanized: Anurādhapuraya; Tamil: அனுராதபுரம், romanized: Aṉurātapuram) is a major city in Sri Lanka. It is the capital city of North Central Province, Sri Lanka and the capital of Anuradhapura District. Anuradhapura is one of the ancient capitals of Sri Lanka, famous for its well-preserved ruins of an ancient Sinhala civilization. It was the third capital of the kingdom of Rajarata, following the kingdoms of Tambapanni and Upatissa Nuwara. <br>
                    * The city, now a World Heritage site, was the centre of Theravada Buddhism for many centuries. The city lies 205 km (127 mi) north of the current capital of Colombo in the North Central Province, on the banks of the historic Malvathu River. It is one of the oldest continuously inhabited cities in the world and one of the eight World Heritage Sites of Sri Lanka. <br>
                    <h2 class="content-title">Buddhism and Anuradhapura</h2>
                    * Anuradhapura was a major intellectual centre for early Theravāda Buddhism, home to revered Buddhist philosophers including Buddhaghosa. <br>
                    * During the reign of Dhatusena (455-473) a redaction of the Theravada Buddhist canon took place while at the same time 18 new vihara (temple complexes) where built and a statue erected for Mahinda, the Indian prince-monk who introduced Buddhism to the island. <br>
                    * During the late Anuradhapura period, the royal family and nobility of Sri Lanka strongly supported Buddhism. As such, they frequently commissioned works of art and donated these items to Buddhist temples. In return, the temple and local Buddhist community supported the king's rule. Art works featuring depictions of Avalokitesvara, the Bodhisattva of Mercy and Compassion, became increasing popular. <br>

                    <h2 class="content-title">European discovery</h2>
                    * The area was uninhabited for many centuries, but the local population remained aware of the ruins. In Robert Knox's 1681 An Historical Relation of the Island Ceylon, he wrote: "At this City of Anurodgburro is a Watch kept, beyond which are no more people that yield obedience to the King of Candy".[5] In 1821, John Davy wrote that: "Anooradapoora, so long the capital of Ceylon, is now a small mean village, in the midst of a desert. A large tank, numerous stone pillars, two or three immense tumuli, (probably old dagobahs,) are its principal remains. It is still considered a sacred spot; and is a place of pilgrimage." <br>

