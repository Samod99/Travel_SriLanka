                <h2 class="content-title">Location</h2>
                <p class="paragraph-text">* Polonnaruwa, is located at a distance of 216km from Colombo in the North central province of Sri Lanka. <br>

                    <h2 class="content-title">History</h2>
                    * Polonnaruwa was first established as a military post by the Sinhalese kingdom.[1] And, it was renamed Jananathamangalam by the Chola dynasty after their successful invasion of the country in the 10th century.[11] Under this period systematic destruction of the Buddhist civilisation took place in the northern plains of Sri Lanka. Raja Raja Chola I built Vanavan Mahadevisvaram, a Shiva temple at Polonnaruwa named after his queen, which presently is known as Siva Devale.[12] The temple among other contained Ganesha and Parvati statues of bronze.[7] north and central parts of Sri Lanka was under this period ruled under Rajendra Chola I directly as a Chola province. However, following the year 1070 AD ended the Chola rule in the island, and Polonnaruwa was captured by Vijayabahu I of Polonnaruwa also known as Vijayabahu the great. br
                    * Starting from Mahanagakula on the south of the Walawe river, Vijayabahu dispatched three armies to attack Polonnaruwa from three fronts. One army was sent along the western shore of the country to Mahathittha port to deal with any reinforcements arriving from South India. Afterwards, part of this army moved towards Polonnaruwa and attacked from the North-west, while the other part held the ports to prevent reinforcements from arriving. A second army was sent from the east across Magama to attack Polonnaruwa from the east. The third and main force advanced across the country, led by the king. Surrounded by these three armies, Polonnaruwa was besieged for seven months before king Vijayabahu’s forces entered the city. In 1070, Vijayabahu became the ruler of Polonnaruwa.[14][15] At that time Sri Lanka was known as Thambapanni. <br>
                    * Trade and agriculture flourished under the patronage of the famous grand son of king Vijayabahu I of Polonnaruwa, king Prakramabahu the great, who was so adamant that no drop of water falling from the heavens was to be wasted and each was to be used toward the development of the land.[citation needed] Hence, irrigation systems that are far superior to those of the Anuradhapura Age were constructed during Parakramabahu's reign – systems which to this day supply the water necessary for paddy cultivation during the scorching dry season in the east of the country. The greatest of these systems is the Parakrama Samudra or the Sea of Parakrama which were also used as a large sea going ship anchorage via Mahaweli River.[13] The Kingdom of Polonnaruwa was completely self-sufficient during King Parakramabahu's reign <br>
                    * With the exception of his immediate successor, Nissankamalla I, all other monarchs of Polonnaruwa were slightly weak-willed and rather prone to picking fights within their own court.[citation needed] They also went on to form more intimate matrimonial alliances with stronger South Indian kingdoms until these matrimonial links superseded the local royal lineage. This prompted an invasion by the Aryacakravarti dynasty war load Kalinga Magha in 1214, who saw the complete destruction of the metropolises of Anuradhapura and Polonnaruwa by burning. Kalinga Magha by the time of his defeat had destroyed the Buddhist civilization in north of Sri Lanka. <br>
                    <h2 class="content-title">Reaching Polonnaruwa</h2>
                    * Polonnaruwa can be reached by main road Colombo-Habarana main road (A6) and then by Habarana-Polonnaruwa (A11) main road. Colombo-Batticaloa railway line runs via Polonnaruwa. The railway station as well as the main bus station is 4km straight east of the town center at Kaduruwela. <br>

                    <h2 class="content-title">Accommodation</h2>
                    * Accommodation at Polonnaruwa is modest and as such the village of Giritale, located15km down the main road, with its lovely hotels perched on banks of beautiful Giritale lake has become the much preferred option. Habarana, a further 35km down the road is still superior; once again with Habarana lake and loveliest eco-oriented high end hotels.Then again, Polonnaruwa's rest houses by the great man-made lake have their own charms to please all comers. <br>

                    <h2 class="content-title">Polonnaruwa Old Town</h2>
                    * Polonnaruwa Old Town is where the well preserved ruins of the ancient city of Polonnaruwa, a world heritage site, are located. The ruins are clustered over an extensive area which runs s about 4km from north to the south. Among the multitude of ruins, Citadel, Quadrangle, Gal Vihara (Sinhala: stone temple) and Lanktilaka, Gal Pota (Sinhala: stone book), The Menik and Rankoth vihara Buddhist shrines, Alahena Pirivena monastery, Lankatilaka (Sinhala: Ornament of Lanka), Kiri Vehera stupa, Tivanka-patamaghara image house Buddhist shrine, are must visit attractions.
                    To the west of the ancient city is inland-sea like Parakrama Samudra (Sinhala: sea of Parkramabahu) man-made lake built in the 13th century and rehabilitated in 1950s. <br>

                    <h2 class="content-title">Polonnaruwa Museum</h2>
                    * Polonnaruwa Museum is the ideal starting point in touring the ancient city of Polonnaruwa: the impressive presentation at the museum imparts a wealth of knowledge that would help come to grips with numerous ruins, preserved ruins, restored ruins and rehabilitated irrigation network that consists of man-made rainwater reservoirs and canals that distribute water to the agricultural district of Polonnaruwa.
                    
