                <h2 class="content-title">Sigiriya</h2>
                <p class="paragraph-text">* Sigiriya or Sinhagiri (Lion Rock Sinhala: සීගිරිය, Tamil: சிகிரியா/சிங்ககிரி, pronounced see-gi-ri-yə) is an ancient rock fortress located in the northern Matale District near the town of Dambulla in the Central Province, Sri Lanka. The name refers to a site of historical and archaeological significance that is dominated by a massive column of rock around 180 metres (590 ft) high. <br>
                    * According to the ancient Sri Lankan chronicle the Culavamsa, this area was selected by King Kashyapa (477 – 495 AD) for his new capital. He built his palace on the top of this rock and decorated its sides with colourful frescoes. On a small plateau about halfway up the side of this rock he built a gateway in the form of an enormous lion. The name of this place is derived from this structure — Sīnhāgiri, the Lion Rock (an etymology similar to Sinhapura, the Sanskrit name of Singapore, the Lion City). <br>
                    * The capital and the royal palace were abandoned after the king's death. It was used as a Buddhist monastery until the 14th century.[3] Sigiriya today is a UNESCO listed World Heritage Site. It is one of the best preserved examples of ancient urban planning. <br>

                    <h2 class="content-title">Archaeological remains and features</h2>
                    * In 1831 Major Jonathan Forbes of the 78th Highlanders of the British Army, while returning on horseback from a trip to Pollonnuruwa, encountered the "bush covered summit of Sigiriya".[9] Sigiriya came to the attention of antiquarians and, later, archaeologists. Archaeological work at Sigiriya began on a small scale in the 1890s. H.C.P. Bell was the first archaeologist to conduct extensive research on Sigiriya. The Cultural Triangle Project, launched by the Government of Sri Lanka, focused its attention on Sigiriya in 1982. Archaeological work began on the entire city for the first time under this project. There was a sculpted lion's head above the legs and paws flanking the entrance, but the head collapsed years ago. <br>
                    * Sigiriya consists of an ancient citadel built by King Kashyapa during the 5th century. The Sigiriya site contains the ruins of an upper palace located on the flat top of the rock, a mid-level terrace that includes the Lion Gate and the mirror wall with its frescoes, the lower palaces clings to the slopes below the rocks. The moats, walls and gardens of the palace extended for a few hundred metres from the base of the rock. The site was both a palace and a fortress. The upper palace on the top of the rock includes cisterns cut into the rock. <br>
                    <h2 class="content-title">Mirror wall</h2> 
                    * Originally this wall was so highly polished that the king could see himself whilst he walked alongside it. Made of brick masonry and covered in highly polished white plaster, the wall is now partially covered with verses scribbled by visitors, some of them dating from as early as the 8th century. Most, however, date from the 9th and 10th century. People of all walks of life, from poets to provincial governors to housewives, wrote on the wall [1]. Even bhikkhus were not exempt; they wrote poetry on varying subjects such as love, irony, and experiences of all sorts. This is the only evidence of poetry found in the Anuradhapura period. <br>
                    * 
                    <h2 class="content-title">Boulder gardens</h2>
                    * The boulder gardens consist of several large boulders linked by winding pathways. The gardens extend from the northern slopes to the southern slopes of the hills at the foot of Sigiris rock. Most of these boulders had a building or pavilion upon them; there are cuttings that were used as footings for brick walls and beams. They were used to be pushed off from the top to attack enemies when they approached. <br>
                    <h2 class="content-title">Water gardens</h2>
                    * The water gardens can be seen in the central section of the western precinct. Three principal gardens are found here. The first garden consists of a plot surrounded by water. It is connected to the main precinct using four causeways, with gateways placed at the head of each causeway. This garden is built according to an ancient garden form known as char bagh, and is one of the oldest surviving models of this form. <br>
                    * The second contains two long, deep pools set on either side of the path. Two shallow, serpentine streams lead to these pools. Fountains made of circular limestone plates are placed here. Underground water conduits supply water to these fountains which are still functional, especially during the rainy season. Two large islands are located on either side of the second water garden. Summer palaces are built on the flattened surfaces of these islands. Two more islands are located farther to the north and the south. These islands are built in a manner similar to the island in the first water garden. <br>
                    * The third garden is situated on a higher level than the other two. It contains a large, octagonal pool with a raised podium on its northeast corner. The large brick and stone wall of the citadel is on the eastern edge of this garden. <br>
                    * The water gardens are built symmetrically on an east-west axis. They are connected with the outer moat on the west and the large artificial lake to the south of the Sigiriya rock. All the pools are also interlinked using an underground conduit network fed by the lake, and connected to the moats. A miniature water garden is located to the west of the first water garden, consisting of several small pools and watercourses. This recently discovered smaller garden appears to have been built after the Kashyapan period, possibly between the 10th and 13th centuries. <br>
                    <h2 class="content-title">Frescoes</h2>
                    * John Still in 1907 wrote, "The whole face of the hill appears to have been a gigantic picture gallery... the largest picture in the world perhaps".[12] The paintings would have covered most of the western face of the rock, an area 140 metres long and 40 metres high. There are references in the graffiti to 500 ladies in these paintings. However, most have been lost forever. More frescoes, different from those on the rock face, can be seen elsewhere, for example on the ceiling of the location called the "Cobra Hood Cave". <br>
                    * Although the frescoes are classified as in the Anuradhapura period, the painting style is considered unique;[13] the line and style of application of the paintings differing from Anuradhapura paintings. The lines are painted in a form which enhances the sense of volume of the figures. The paint has been applied in sweeping strokes, using more pressure on one side, giving the effect of a deeper colour tone towards the edge. Other paintings of the Anuradhapura period contain similar approaches to painting, but do not have the sketchy lines of the Sigiriya style, having a distinct artists' boundary line. The true identity of the ladies in these paintings still have not been confirmed. There are various ideas about their identity. Some believe that they are the ladies of the kings while others think that they are women taking part in religious observances. These pictures have a close resemblance to paintings seen in the Ajanta Caves in India. <br>

