                        <h2 class="content-title">Yala National Park</h2>
                        <p class="paragraph-text">* Yala (යාල) National Park is the most visited and second largest national park in Sri Lanka, bordering the Indian Ocean. <br>
                            * The park consists of five blocks, two of which are now open to the public, and also adjoining parks. <br>
                            * The blocks have individual names such as, Ruhuna National Park (Block 1), and Kumana National Park or 'Yala East' for the adjoining area. <br>
                            * It is situated in the southeast region of the country, and lies in Southern Province and Uva Province. 
                            * The park covers 979 square kilometres (378 sq mi) and is located about 300 kilometres (190 mi) from Colombo. <br>
                            * Yala was designated as a wildlife sanctuary in 1900, and, along with Wilpattu was one of the first two national parks in Sri Lanka, having been designated in 1938. <br>
                            * The park is best known for its variety of wild animals. <br>
                            * It is important for the conservation of Sri Lankan elephants, Sri Lankan leopards and aquatic birds. <br>
                            * There are six national parks and three wildlife sanctuaries in the vicinity of Yala. <br>
                            * Among the largest is Lunugamvehera National Park. <br>
                            * The park is situated in the dry semi-arid climatic region and rain is received mainly during the northeast monsoon. <br>
                            * Yala hosts a variety of ecosystems ranging from moist monsoon forests to freshwater and marine wetlands. <br>
                            * It is one of the 70 Important Bird Areas (IBAs) in Sri Lanka. <br>
                            * Yala harbours 215 bird species including six endemic species of Sri Lanka. <br>
                            * The number of mammals that has been recorded from the park is 44, and it has one of the highest leopard densities in the world. <br>
                            * The area around Yala has hosted several ancient civilizations. <br>
                            * Two important pilgrim sites, Sithulpahuwa and Magul Vihara, are situated within the park. <br>
                            * The 2004 Indian Ocean tsunami caused severe damage on the Yala National Park and 250 people died in its vicinity. <br>
                            * The number of visitors has been on the rise since 2009, after the security situation in the park improved. <br>
                            <h2 class="content-title">Elephant gathering</h2>
                            * The Yala National Park is the most visited park in Sri Lanka. <br>
                            * In 2002 around 156,867 tourists visited the park. <br>
                            * Foreigners, especially Europeans, account for 30% of total visitors. <br>
                            * Block I is the main area for visits. <br>
                            * Block III (main gate in Galge area, on Buttala-Kataragama Road) and the adjoining Kumana Park or 'Yala East' (main gate at Okanda, on the east coast not far from Pottuvil) however are becoming popular in their own right too. <br>
                            * Note that the Situlpahuwa pilgrimage site, geographically in Block III, has kind of an 'enclave' status and is accessible FOC through separate roads from Tissa and Kataragama. <br>
                            * Most of the visitors stated that reasons for their visit is to see wild animals, and elephant is the most preferred animal. <br>
                            * The visitors like to see bears, leopards, birds as well. <br>
                            * In 2000 the income from visitors including lodge fees was approximately US$468,629. <br>
                            * Due to security conditions revenue was lost. <br>
                            * The Yala National Park has been susceptible to terrorist attacks. <br>
                            * On 17 October 2007 a group of LTTE cadres attacked an army detachment in Thalgasmankada in the park. <br>
                            * The attack killed six army soldiers and another was caught up in a landmine explosion. <br>
                            * On 11 July 2008 four people died in an attack launched by the LTTE. <br>
                            * The cadres opened fire at a bus carrying pilgrims to Kataragama. <br>
                            * Since the end of the civil war, May 2009, no violence has occurred in Yala area also and it is fully safe for visitors; this was also the main factor in opening blocks III and V for tourists. <br>
                            * From January to June in 2008, 9,078 local tourists and 7,532 foreigners have visited Yala. <br>
                            * For the same period of time in 2009 the arrivals have risen to 18,031 locals and foreigners to 10,439. <br>
                            * Accordingly, the revenue increased to Rs. 27 million (US$235,000) in 2009 from Rs. 16.6 million (US$154,000) in 2008. <br>
                            * The visitors are allowed to see the wild animals from 5.30 am to 6.30 pm. <br>
                            * Due to droughts the park used to be closed to tourists from 1 September, to 15 October annually; however in 2009 and 2010 the closure was skipped and lakes filled with water bowsers for drinking water for the animals, a future strategy on drought handling is not yet clear.


