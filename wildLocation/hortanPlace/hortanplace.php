                <h2 class="content-title">Horton Plains National Park</h2>
                <p class="paragraph-text">* Horton Plains National Park (Sinhala: හෝර්ටන් තැන්න ජාතික උද්‍යානය Hortan Thanna Jathika Udyanaya) is a national park in the central highlands of Sri Lanka that was designated in 1988. 
                    * It is located at an elevation of 2,100–2,300 m (6,900–7,500 ft) and encompasses montane grassland and cloud forest. <br>
                    * It is rich in biodiversity and many species found here are endemic to the region. <br>
                    * It is also a popular tourist destination and is situated 8 kilometres (5.0 mi) from Ohiya, 6 kilometres
                     (3.7 mi) from the world-famous Ohiya Gap/Dondra Watch and 32 kilometres (20 mi) from Nuwara Eliya. <br>
                    * The Horton Plains are the headwaters of three major Sri Lankan rivers, the Mahaweli, Kelani, and Walawe. <br>
                    * In Sinhala the plains are known as Maha Eliya Plains (මහ එළිය තැන්න). <br>
                    * Stone tools dating back to Balangoda culture have been found here. <br>
                    * The plains' vegetation is grasslands interspersed with montane forest and includes many endemic woody plants. <br>
                    * Large herds of Sri Lankan sambar deer feature as typical mammals and the park is also an Important Bird Area with many species not
                    only endemic to Sri Lanka but restricted to the Horton Plains. <br>
                    * Forest dieback is one of the major threats to the park and some studies
                    suggest that it is caused by a natural phenomenon. <br>
                    * The sheer precipice of World's End and Baker's Falls are among the tourist attractions of the park. <br>
                    * In 20th century there are some records of elephants again in the park <br>
                    <h2 class="content-title">Tourist attractions</h2>
                    * Horton Plains is a popular tourist destination, with World's End being the key attraction. <br>
                    * In the six months ending in August 2009, Horton Plains National Park earned a revenue of Rs. 20.1 million (US$ 0.17 million). <br>
                    * The park is accessed by the Nuwara Eliya-Ambewela-Pattipola and Haputale-Boralanda roads, and there are railway stations at Ohiya and Ambewela. <br>
                    * World's End is a sheer precipice with a 870 m (2,854 ft) drop. It is situated at the southern boundary of the park. <br>
                    * Another cliff known as the Lesser World's End of 270 m (886 ft) is located not far from World's End. <br>
                    * Baker's Falls, a waterfall formed by Belihul Oya, a tributary of the Walawe River is named after Sir Samuel Baker,
                    a hunter and explorer who attempted to establish a European agricultural settlement at Nuwara Eliya. <br>
                    * The waterfall is 20 metres (66 ft) high. <br>
                    * Slab Rock Falls is another well-known waterfall in the plains. <br>
                    * The waterfall can be reached by walking on one of the main trails; the trail is a bit steep at the end but the difficulty level is medium to easy.
