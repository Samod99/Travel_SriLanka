                <h2 class="content-title">Minneriya National Park</h2>
                <p class="paragraph-text">* Minneriya National Park (Sinhala: මින්නේරිය ජාතික වනෝද්‍යානය, romanized: Minnēriya Jātika Vanōdyānaya; Tamil: மின்னேரியா தேசிய வனம், romanized: Miṉṉēriyā Tēciya Vaṉam) is a national park in North Central Province of Sri Lanka. <br>
                    *  The area was designated as a national park on 12 August 1997, having been originally declared as a wildlife sanctuary in 1938. The reason for declaring the area as protected is to protect the catchment of Minneriya tank and the wildlife of the surrounding area. <br>
                    *  The tank is of historical importance, having been built by King Mahasen in third century AD. The park is a dry season feeding ground for the elephant population dwelling in forests of Matale, Polonnaruwa, and Trincomalee districts. <br>
                    *  The park earned revenue of Rs. 10.7 million in the six months ending in August 2009. Along with Kaudulla and Girithale, Minneriya forms one of the 70 Important Bird Areas (IBAs) of Sri Lanka. The park is situated 182 kilometres (113 mi) from Colombo.
                    <h2 class="content-title">Elephant gathering</h2>
                    * Large numbers of Sri Lankan elephants are attracted to grass fields on the edges of the reservoir during the dry season. The Minneriya tank contributes to sustain a large herd. Elephants gathered here is numbering around 150–200. Some reports account number of elephants to as high as 700. They migrate here from Wasgamuwa National Park and benefit from food and shelter of the park's forest. Tourists visit Minneriya largely because of elephants, especially in dry season <br>
                    <h2 class="content-title">Other animals</h2>
                    * The park is an important habitat for the two endemic monkeys of Sri Lanka: purple-faced langur and toque macaque. Large herbivorous mammals such as Sri Lankan sambar deer and Sri Lankan axis deer frequent the park. Rare and endangered species such as Sri Lankan leopard and Sri Lankan sloth bear inhabit in Minneriya. Minneriya is one of the areas where the gray slender loris is reportedly found in Sri Lanka.
                </p>
            
