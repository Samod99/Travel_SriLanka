                <h2 class="content-title">Udawalawe National Park</h2>
                <p class="paragraph-text">* Udawalawe National Park lies on the boundary of Sabaragamuwa and Uva Provinces, in Sri Lanka. The national park was created to provide a sanctuary for wild animals displaced by the construction of the Udawalawe Reservoir on the Walawe River, as well as to protect the catchment of the reservoir. The reserve covers 30,821 hectares (119.00 sq mi) of land area and was established on 30 June 1972. Before the designation of the national park, the area was used for shifting cultivation (chena farming). The farmers were gradually removed once the national park was declared. The park is 165 kilometres (103 mi) from Colombo. Udawalawe is an important habitat for water birds and Sri Lankan elephants. It is a popular tourist destination and the third most visited park in the country. <br>
                    <h2 class="content-title">Birds</h2>
                    * Udawalawe is also a good birdwatching site. Endemics such as Sri Lanka spurfowl, red-faced malkoha, Sri Lanka grey hornbill, brown-capped babbler, and Sri Lanka junglefowl are among of the breeding resident birds. White wagtail and black-capped kingfisher are rare migrants. A variety of water birds visit the reservoir, including cormorants, the spot-billed pelican, Asian openbill, painted stork, black-headed ibis and Eurasian spoonbill. <br>
                    * The open parkland attracts birds of prey such as white-bellied sea eagle, crested serpent-eagle, grey-headed fish eagle, booted eagle, and changeable hawk-eagle. Landbirds are in abundance, and include Indian roller, Indian peafowl, Malabar pied hornbill and pied cuckoo. 
                    <h2 class="content-title">Tourism</h2>
                    * In the six months ending in August 2009, the park earned revenue of Rs. 18.2 million. From 1994 to 2001 some 423,000 people visited the park, 20% of them foreigners. For the 1998-2001 period average annual revenue was US$280,000. Postage stamps bearing the images of four wild animals of Udawalawe were issued on 31 October 2007 in the series "National Parks of Sri Lanka", featuring water buffalo, Sri Lankan elephant, ruddy mongoose and tufted grey langur.
