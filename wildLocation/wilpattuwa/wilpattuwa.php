                <h2 class="content-title">Wilpattu National Park</h2>
                <p class="paragraph-text">* Wilpattu National Park (Willu-pattu; Land of Lakes) is a park located on the island of Sri Lanka. <br>
                    *  The unique feature of this park is the existence of "Willus" (natural lakes) - natural, sand-rimmed water basins or depressions that fill with rainwater. <br>
                    * Located on the northwest coast lowland dry zone of Sri Lanka. <br>
                    * The park is located 30 km (19 mi) west Anuradhapura and 26 km (16 mi) north of Puttalam (approximately 180 km (110 mi) north of Colombo). <br>
                    * The park is 1,317 km2 (508 sq mi) (131, 693 hectares) and ranges from 0–152 m (0–499 ft) above sea level. <br>
                    * Nearly one hundred and six lakes (Willu) and tanks are found spread throughout Wilpattu. <br>
                    * Wilpattu is the largest and one of the oldest National Parks in Sri Lanka. <br>
                    * Wilpattu is among the top national parks world-renowned for its leopard (Panthera pardus kotiya) population. <br>
                    * A remote camera survey was conducted in Wilpattu from July to October 2015 by the Wilderness and Wildlife Conservation Trust. <br>
                    * A sample of forty nine individual leopards were photo-captured in the surveyed area and the core area density was between that of Yala National Park's Block I and Horton Plains National Park. <br>
                    * From December 1988 to 16 March 2003, the park was closed due to security concerns surrounding the Sri Lankan Civil War, before being reopened to visitors sixteen years later. <br>
                    * Visitor access is currently limited to approximately 25% of the park, the remainder of which is dense forest or scrub. <br>
                    * Popular visiting periods spans between the months of February and October, although there are a number of private eco-tourism groups that conducts safaris year round. <br>
                    <h2 class="content-title">Birds</h2>
                    * The painted stork, the open bill, little cormorant, Sri Lankan junglefowl (Gallus lafayetii) along with many species of owls, terns, gulls, eagles, kites buzzards are to be found at Wilpattu National Park. <br>
                    * Wetland bird species that can be seen in Wilpattu are the garganey (Anas querquedula), pintail (Anas acuta), whistling teal (Dendrocygna javanica), spoonbill (Platalea leucorodia), black-headed ibis (Threskiornis malanocephalus), large white egret (Egretta alba modesta), cattle egret (Bubulcus ibis) and purple heron (Ardea purpurea). <br>
                    <h2 class="content-title">Reptiles</h2> 
                    * The most common reptiles found in the park are the monitor lizard (Varanus bengalensis), mugger crocodile (Crocodylus palustris), common cobra (Naja naja), rat snake (Ptyas mucosus), Indian python (Python molurus), pond turtle (Melanonchelys trijuga) and the soft shelled turtle (Lissemys punctata) which are resident in the large permanent Villus.
