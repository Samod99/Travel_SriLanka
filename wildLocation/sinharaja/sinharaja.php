                <h2 class="content-title">Sinharaja Forest Reserve</h2>
                <p class="paragraph-text">* Sinharaja Forest Reserve is a forest reserve and a biodiversity hotspot in Sri Lanka. It is of international significance and has been designated a Biosphere Reserve and World Heritage Site by UNESCO. <br>
                    * According to International Union for Conservation of Nature (IUCN), Sinharaja is the country's last viable area of primary tropical rainforest. More than 60% of the trees are endemic and many of them are considered rare. 50% of Sri Lankan's endemics species of animals (especially butterfly, amphibians, birds, snakes and fish species). It is home to 95% endemic birds. <br>
                    * The hilly virgin rainforest, part of the Sri Lanka lowland rain forests ecoregion, was saved from the worst of commercial logging by its inaccessibility, and was designated a World Biosphere Reserve in 1978 and a World Heritage Site in 1988. <br>
                    * Because of the dense vegetation, wildlife is not as easily seen as at dry-zone national parks such as Yala. There are about 3 elephants, and 15 or so[vague] leopards. The most common larger mammal is the endemic purple-faced langur. <br>
                    * Birds tend to move in mixed feeding flocks, invariably led by the fearless Sri Lanka Crested Drongo and the noisy orange-billed babbler. Of Sri Lanka's 26 endemic birds, the 20 rainforest species all occur here, including the elusive red-faced malkoha, green-billed coucal and Sri Lanka blue magpie. <br>
                    * Reptiles include the endemic green pit viper and hump-nosed vipers, and there are a large variety of amphibians, especially tree frogs. Invertebrates include the endemic common birdwing butterfly and leeches. <br>
                    <h2 class="content-title">Location</h2>
                    * Sinharaja Forest Reserve covers most of the Kalu Ganga basin and a small part of northern Gin Ganga, most of the forest, 60% is contained within the borders of Rathnapura District. The other parts include Galle District with 20% and Kaluthara District with 20%.
                    <h2 class="content-title">Natural</h2>
                    * The rainforest likely formed during the Jurassic era (from 200 million years to 145 million years ago). This forest encompasses a span of 36,000 hectares (88,960 acres/360 km²).[3] The reserve is only 21 km (13 mi) from east to west, and a maximum of 7 km (4.3 mi) from north to south, but it is a treasure trove of endemic species, including trees, insects, amphibians, reptiles, birds, and mammals. Sinharaja forest vegetation density has been around 240,000 plants per hectare, the most dense rain forest in Asia.
