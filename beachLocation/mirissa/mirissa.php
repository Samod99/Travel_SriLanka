                <h2 class="content-title">Mirissa</h2>
                <p class="paragraph-text"> * Mirissa (Sinhala: මිරිස්ස, romanized: Mirissa; Tamil: மிரிஸ்ஸ, romanized: Miris'sa) is a small town on the south coast of Sri Lanka, located in the Matara District of the Southern Province. It is approximately 150 kilometres (93 mi) south of Colombo and is situated at an elevation of 4 metres (13 ft) above sea level. Mirissa's beach and nightlife make it a popular tourist destination. It is also a fishing port and one of the island's main whale and dolphin watching locations. <br>
                    <h2 class="content-title">Whale and Dolphin watching</h2>
                    * There are 03 known areas of the sea that Whales and Dolphins can be observed in Sri Lanka; Mirissa, Trincomalee and Kalpitiya. Mirissa is quite famous since most sightings have occurred in the Weligama-Mirissa-Dondra area. <br>
                    <h2 class="content-title">Surfing in Mirissa</h2>
                    * Mirissa is a trending surf point in Sri Lanka. The crescent-shaped Mirissa beach is ideal for intermediate to experienced surfers. The break is located to the west of the beach along the left curve of the bay. Absolute beginners can try the Weligama surf point where it has beach breaks. <br>
                    * The point break waves are pretty consistent with decent swell direction. The swells hold up to 3-5 feet. <br>
                    <h2 class="content-title">Mirissa beach</h2>
                    * The crescent-shaped Mirissa beach is located very close to the town can be accessed directly by the Galle-Matara main road at some points. It stretches along the largest bay of Mirissa close to a kilometer from the rocky headlands of the west to the Parrot rock to the east. <br>
                    * Once you’re on the beach looking at the horizon, it is shuddering to think there are no lands between here and Antarctica. <br>
                    * Even though it used to be a hidden little beach the word has been out for too long now it is hoarded with beach beds, parasols and um.. tourists. <br>
                    * There are, however, not many locals hanging around and none of the annoying beach boys to sell you god knows what. <br>
                    * If you’re coming from town, hit Sunanda road just opposite the town’s main bus stop and turn left to the beachside road and go about 120m. There is a shortcut to the right which ends up in the surfing area of the beach. Beach is also accessible via many beachside restaurants and bars. <br>
                    * Many guesthouses and bars are located from 500m to 01km from the town towards Matara. The beach can be accessed by the main road thence. <br>
                    <h2 class="content-title">The “Secret Beach”</h2>
                    * This not so secret “Secret beach” is located just west to the Mirissa beach although it cannot be accessed directly from there. The pioneering people who found this beach must’ve named it thus for lack of a better word but the word has certainly gone around. <br>
                    * This little, secluded beach, however, is less crowded and more tranquil than the famous Mirissa beach. There are no sunbeds, noisy beach bars, and hordes of tourists. No sir. <br>
                    <h2 class="content-title">The Coconut Tree Spot</h2>
                    * Not only the hill country but southern beaches also have viewpoints. The coconut tree spot is one such viewpoint located at the extreme eastern corner of the 03rd bay of Mirissa. This headland affords panoramic vistas over the Mirissa beach and another perfect place to chill and watch the sunset. <br>
                    <h2 class="content-title">The Parrot Rock</h2>
                    * When you’re at Mirissa beach, you simply can’t miss it. It is the mound of land that sits there to the left of the beach separated only by a thin strip of sand  (Google Maps). The name derives from the shape of the rock which is about 50 ft in height. <br>
                    * To get to the rock, you’ll have to wade through a small strip of sea. The depth depends on the tide . Probably you can get away with getting wet only below your waist. There seem to have been a proper pathway before but it has succumbed to the wrath of the sea. <br>
                    * The path to the small hill is a barely visible stairway and can be a bit tricky hence don’t try this after consuming alcohol. <br>
                    * Once at the top spend your time to capture that perfect insta-moment. You’ll be blessed with 360 views of entire Mirissa beach and surroundings. <br>
                    * Make sure to capture the sunset while you’re at it. If you have got a drone, this is a perfect place to fly it. Watch out for crows though. <br>
                    * Please be careful while you’re trying reach edges since this place has not proper railings and such.
