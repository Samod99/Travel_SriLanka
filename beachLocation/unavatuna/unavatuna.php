                <h2 class="content-title">Unawatuna</h2>
                <p class="paragraph-text">* Unawatuna is a coastal town in Galle district of Sri Lanka. Unawatuna is a major tourist attraction in Sri Lanka and known for its beach and corals. It is a suburb of Galle, about 5 kilometres (3.1 mi) southeast to the city center and approximately 108 kilometres (67 mi) south of Colombo. Unawatuna is situated at an elevation of 5 metres (16 ft) above the sea level. Despite significant development in the last decade it is still home to the endangered and endemic purple-faced langur, an usually shy monkey species that can only be found in Sri Lanka's forests. <br>
                     <h2 class="content-title">Eco-Tourism</h2>
                     * Unawatuna is rich in its biodiversity. Its greatest potential attraction for eco-tourism was the marsh land or mangrove, Kadolana, which was partially dredged and filled to develop a hotel for a German hotel chain. The development never eventuated. Many locals believe it to have been cursed for being built at the doorstep of the Wella Devalaya. <br>
                     * Over sixty species of endemic birds, including terns, egrets, herons, sandpipers and kingfishers, as well as rarer species such as the lesser whistling duck, the Asian palm swift, the white-breasted waterhen, the Loten's sunbird and the black bittern have been sighted in the locality by the ornithologist, Clive Byers. These birds are mostly sighted in the remaining marshy area and Rumassala Hillock. <br>
                     * Off the coast of Unawatuna, beneath the Indian Ocean lies a number of coral reefs, shipwrecks, and a great variety of fish and turtles. The turtles still wade onto the shore to lay their nests and eggs, and at times, as if to lay first claim to the sandy shore now invaded by the tourists and dotted by restaurateurs, even go right into the beach front restaurants . <br>
                     * The Rumassala coral reefs at the east end of the Galle Harbor attract divers, but are now endangered due to possible port development.[citation needed] Eco treks in the shrub jungles of Rumassala are also available.
                     <h2 class="content-title">Transport</h2>
                     * Unawatuna is located on the Coastal or Southern Rail Line (connecting Colombo through to Matara), and the A2 highway, connecting Colombo to Wellawaya. <br>
                     <h2 class="content-title">Attractions</h2>
                     * Unawatuna Beach and coral reefs <br>
                     * Unawatuna Peace Pagoda <br>
                     * Yatagala Raja Maha Viharaya Buddhist Temple <br>
                     * Turtle hatcheries (Habaraduwa) <br>
