                <h2 class="content-title">Hikkaduwa</h2>
                <p class="paragraph-text">* Hikkaduwa is famous for serving delicious seafood curries, relaxing massages, Buddhist temples, and watersports such as surfing, snorkeling, and diving. The beaches in Hikkaduwa may not be at par with Bali or Goa, but it does attract a fair amount of regulars. <br>
                    <h2 class="content-title">Narigama Beach: Watch the Sunset</h2>
                    * The Narigama Beach is the most popular beach in Hikkaduwa and sees the maximum influx of tourists. The Narigama beach stretches on for about 4 kilometers and is a perfect surfing spot during the months from April to October. The sea water is calmer during the winters. The beautiful hues splashed into the sky by the setting sun make the beach appear like a scene from a film or a photograph. Thus, watching the sun dip behind the waters of the Narigama Beach is a treat for the eyes. <br>

                    <h2 class="content-title">Coral Reef Gardens: Explore the underwater world</h2>
                    * The colorful coral reefs are definitely worth a visit before climate change upsets the critical balance of nature. Sri Lankas vibrant coral reefs are home to a range of aquatic wildlife. They house a variety of fishes. Apart from the coral reefs, the sea turtles are yet another major attraction. You could feed these turtles by hand! The turtles often bury their eggs into the sands of the Hikkaduwa beach, and if you are lucky enough you can even watch new hatchlings make their way into the sea! <br>

                    <h2 class="content-title"> Splash Around at the Laguna</h2>
                    * The Laguna is a pool of shallow water covered by coral reefs on either side. These coral reefs break the waves making this portion of the beach devoid of any waves. Thus, it is an ideal spot if you or the kids accompanying you wish to enter the seas. Families flock to this beach to rest and splash around the cool waters to beat the heat. You could even book a catamaran to ferry you around the placid waters. In the wee hours of the morning, you could find exotic fishes around the shallow coral area. <br>

                    <h2 class="content-title">Water sports: Lets have some fun</h2>
                    * The Hikkaduwa beach is suited for water sports such as snorkeling, swimming, surfing, scuba diving, body surfing, and boating. You could book a seat on a boat with a transparent bottom where you can closely observe shoals of fishes and other interesting aquatic creatures. If you wish to observe them more closely in the clear waters, you could sign up for one of the scuba diving or snorkeling courses at the PADI certified dive centers. The carefully planned and monitored trips will ensure that the environment stays unaffected by these activities. <br>

                    <h2 class="content-title">Sunken ships of Hikkaduwa: Witness the shipwrecks</h2> 
                    * If you are looking for a taste of adrenaline filled adventure, then Hikkaduwa can fulfill that wish too! Apart from the various species of fishes and sea turtles, the coast also contains sites for four different shipwrecks, which you can explore! Swim beneath the surface and who knows what hidden gem you can find!
