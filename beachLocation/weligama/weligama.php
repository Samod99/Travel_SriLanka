                <h2 class="content-title">Weligama Bay</h2>
                <p class="paragraph-text">* Weligama Bay, in the Matara District on the South Coast of Sri Lanka, translates to 'Sandy Village' for obvious reasons. Many flock to resorts at the bay for its post-card setting of <br>
                    * clear blue waters, soft sands, and palm trees. The bay’s most prominent feature is the minuscule island of Taprobane, just offshore, virtually invisible under a thick covering of luxuriant trees. Taprobane was owned during the 1930s by the exiled French Count de Maunay, who built the exquisite white villa that still stands, its red-tiled roof poking up through the trees; the whole lot is available for rent via The Sun House in Galle. The prettiest part of the bay is around Taprobane, where dozens of colourful outrigger catamarans pull up on the beach between fishing expeditions; you may be able to negotiate a trip around the bay with one of the local fishermen. <br>
                    <h2 class="content-title">Fishing In Weligama Bay</h2>
                    Weligama Bay is a notorious fishing village, infamous for its traditional use of stilt fishing in which locals make it look easy and comfortable. The practice involves using a horizontal pole embedded into the seafloor, the fishermen then use a rod whilst sat a couple of meters above the water to skillfully target their fish. Spotted herrings and small mackerels are common prizes that end up in a bag tied to the pole or their waist. A mesmerizing sight to see and impossible to miss due to the high number of fishermen who use this tactical method. <br>
                    <h2 class="content-title">Things To Do In Weligama Bay</h2>
                    * Weligama Bay, a town still fairly untouched by mass tourism has a range of things to do. From active activities such as surfing to relaxing activities such as whale watching. <br>

                    <h2 class="content-title">Surfing</h2>
                    * Weligama is a hot spot for surfing and is one of the most reliable surf destinations in Sri Lanka. The gentle waves in certain areas make it ideal for beginners, surf lessons and surfboard hire are made readily available, and if you wish to really go for it there are many surf camps in the area. <br>
                    
                    <h2 class="content-title">Whale and Dolphin Watching</h2>
                    * Mirissa, the sister town of Weligama is a prime spot for whale watching. With a choice of luxury to basic catamaran tours, seeing the big beautiful creatures is easy, relaxing and enjoyable. <br>
                    * Be prepared to see the forever happy and mischievous Dolphins showing off and enjoying the attention from a keen audience. <br>

                    <h2 class="content-title">Coconut Plantations</h2>
                    * You will be amazed that the coconut has so many uses. In Weligama, you can visit Coconut plantations where you will learn all about its uses and see first hand how its fibre can be used for classic crafting such as making mats, ropes and nets. <br>

                    <h2 class="content-title">Snake Farm</h2>
                    * If you are not scared like the rest of us, a visit to the snake farm might be something for you. See the slithery Sri Lankan creatures in their natural habitats and be educated by the local guides about their nature and creepy ways. <br>

                    <h2 class="content-title">Bicycle Tours</h2>
                    * A great and active way to get your bearings around Weligama and to see all there is to offer is by taking a guided bicycle tour. Local guides direct you around the area showing you rubber plantations, spice gardens, hidden temples and many more hidden gems that might have otherwise been missed. <br>

                    <h2 class="content-title">Get Historical at Weligama Bay</h2>
                    * There are tonnes of history embedded in the Weligama Bay area, and if you love your doses of tradition and culture then you are in the right place. Visit the statue of Bodhisattva Avalokitesvara, a detailed and intricate statue carved in the rock from somewhere between the 6th and 9th century. The statue, known locally as Kusta Raja Gala, is believed to represent a King who had leprosy. Supposedly, the King had a vision advising him to drink coconut pulp and water for three months in order to restore his health. Miraculously this cure worked and he had a statue of himself built to commemorate the phenomenon. <br>
                    * For a traditional activity, it is recommended to witness the Devil Dance. Sounding more sinister than it really is, the Devil Dance is a traditional healing dance famous in the South. It is believed to speed up the recovery process when one is injured or suffering from illness.
                    


