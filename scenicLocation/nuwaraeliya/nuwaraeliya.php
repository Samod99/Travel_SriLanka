                <h2 class="content-title">Nuwara Eliya</h2>
                <p class="paragraph-text">* (Sinhala: නුවර එළිය [nuwərə ɛlijə]; Tamil: நுவரெலியா) is a
                     city in the hill country of the Central Province, Sri Lanka. <br>
                     * Its name means "city on the plain (table land)" or "city of light". <br>
                     * The city is the administrative capital of Nuwara Eliya District, with a picturesque landscape and temperate climate. <br>
                     * It is at an altitude of 1,868 m (6,128 ft) and is considered to be the most important location for tea production in Sri Lanka. <br>
                     * The city is overlooked by Pidurutalagala, the tallest mountain in Sri Lanka. <br>
                     * Nuwara Eliya is known for its temperate, cool climate – the coolest area in Sri Lanka.
                        <h2 class="content-title">Climate</h2>
                        * Due to its highland location, Nuwara Eliya has a subtropical highland climate (Köppen climate 
                        classification Cfb),[1] having no pronounced dry season, a monsoon-like cloudy season and with a 
                        mean annual temperature of 16 °C (61 °F). <br>
                        * In the winter months, there can be frost at night, but it warms up rapidly during the day because 
                        of the high sun angle. <br>

                        <h2 class="content-title">Attractions</h2>
                        * The town's attractions include the golf course, trout streams, Victoria Park, and boating or 
                        fishing on Lake Gregory. Victoria Park is an attractive and well-used oasis. It is popular with 
                        birdwatchers at quieter times because of the good opportunities for seeing species, particularly 
                        the Indian blue robin, pied thrush or scaly thrush lurking in the denser undergrowth. The Kashmir 
                        flycatcher is another attractive bird species in the park. <br>
                        * Galway's Land Bird Sanctuary, close to Lake Gregory, is an area of montane forest a few kilometers
                         east of the town. Covering an area of 0.6 km2 it is home to many bird and mammal species endemic to 
                         Sri Lanka, including wild boar and barking deer. <br>
                         *The city is a base for visits to Horton Plains National Park. This is a key wildlife area of an open 
                         grassy woodland. Species found here include the leopard, sambar, and the endemic purple-faced langur.
                          Endemic highland birds include the dull-blue flycatcher, Sri Lanka white-eye, and yellow-eared bulbul.
                           The plains have a well-visited tourist attraction at World's End — a sheer precipice with a 1050 m drop. The 
                           return walk passes the scenic Baker's Falls. Early morning visits are best, both to see the wildlife and to
                            view World's End before mists close in during the later part of the morning. <br>
                            * One of the distinctive features of Nuwara Eliya's countryside is the widespread growing of vegetables, fruit
                             and flowers usually associated with temperate Europe. This "Little England" is covered with terraces growing potatoes, 
                             carrots, leeks, and roses interspersed with tea bushes on the steeper slopes.
