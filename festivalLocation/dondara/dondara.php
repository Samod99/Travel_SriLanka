                <p class="paragraph-text">* The “City of Gods” (Devinuwara) is a small town situated at the extreme southern end of the country , approximately 6.5 kms south-east of Matara, a major city in the Southern Province of Sri Lanka. This place is also known as Dondra. <br>
                    * In the 13th -15th centuries, Devinuwara had been a hub of activity as it was a seaport and a popular place of worship.  Its magnificent temple supported by a roof of gilded copper, which twinkled in the sunshine was no doubt a “lighthouse” in its own way and a support to those who sailed the Indian Ocean at that time. The many stone pillars, sculpted in different styles and sizes, bear witness to the splendid shrines and temples that dominated the Indian Ocean until the latter portion of the 16th century.  According to legend, during King Rawana’s time, Devinuwara was a thriving town and is linked to Indian tale – Ramayana. <br>
                    * Recorded history reveals that the Temple and the Devalaya were constructed by King Dappula I in the 7th century. A Portuguese army led by Thome de Sonza attacked this sacred city in 1587, destroying and plundering all the precious objects in the places of worship. But the Kandyan Kingdom’s King Rajasinha II managed to free Matara from the foe and constructed a Devala for Deity Vishnu at Devinuwara. The Devalaya or Deity Vishnu shrine attracts devotees from all over the nation throughout the year, especially during the Esala Poya period in July. <br>
                    * The origins of the Devinuwara Esala Festival go back to the time of King Parakramabahu II of Dambadenya Kingdom in the 13th century, who is said to have renovated the rundown temple in this revered place and frequently celebrated this festival in honor of Upulvan deity. <br>
                    
